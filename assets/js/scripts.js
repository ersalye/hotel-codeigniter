$(document).ready(function(){

	$('.signin-action').click(function(){

		$('.body-overlay').show();

		$('.model-popup.signin').show();

	});


	$('.signup-action').click(function(){

		$('.body-overlay').show();

		$('.model-popup.signup').show();

	});

	$('.modal-close').click(function(){

		$('.body-overlay').hide();

		$(this).closest('.model-popup').hide();

	});

});